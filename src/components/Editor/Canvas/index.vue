          :key="`border-${item}`"
          :style="getBorderStyle(item)"
        ></div>
        <div
          class="enter-container-border-line"
          v-for="item in enterContainerBorderLine"
          :key="`border-${item}`"
          :style="getBorderStyle(item, true)"
